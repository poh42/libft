#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483647)
		ft_putstr("-2147483647");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n > 10)
	{
		ft_putnbr(n/10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
