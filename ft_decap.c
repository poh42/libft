#include "libft.h"

char	*ft_decap(char *c)
{
	int		i;
	char	sub;

	i = 0;
	while (c[i])
	{
		sub = c[i];
		if (c[i] >= 'A' && c[i] <= 'Z')
			c[i] = sub + 32;
		i++;
	}
	return (c);
}
