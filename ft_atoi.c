#include "libft.h"

int	ft_atoi(const char *str)
{
	int		sign;
	int		res;
	int		i;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		sign = ',' - str[i];
	while (str[i + 1] >= '0' && str[i + 1] <= '9')
	{
		res = res * 10 + (str[i + 1] - '0');
		i++;
	}
	return (res * sign);
}
