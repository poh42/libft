#include "libft.h"

char	*ft_capslock(char *c)
{
	int		i;
	char	sub;

	i = 0;
	while (c[i])
	{
		sub = c[i];
		if (c[i] >= 'a' && c[i] <= 'z')
			c[i] = sub - 32;
		i++;
	}
	return (c);
}
