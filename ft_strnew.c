#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*p;
	size_t	i;
	
	i = 0;
	if(!(p = (char*)malloc(size)))
		return (NULL);
	while (size >= i)
	{
		p[i] = '\0';
		i++;
	}
	return (p);
}
